package beginnersProgram;

public class NestedLoopConcept {

	public static void main(String[] args) {
		// doubt
		int x = 0;
		int y = 0;
		if (x > 0) {
			if (y > 0) {
				System.out.println("Both X & Y are positive number");
			} else {
				System.out.println("X is positive number and Y is negative number");
			}
		} else {
			System.out.println("X is not positive number");
		}
	}

}
